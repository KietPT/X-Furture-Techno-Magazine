package kietpt.dto;

/**
 *
 * @author KietPT
 */
public class CategoryDTO {
     int id,status;
     String name,ID;

    public CategoryDTO(int id, int status, String name) {
        this.id = id;
        this.status = status;
        this.name = name;
    }

    public CategoryDTO() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
